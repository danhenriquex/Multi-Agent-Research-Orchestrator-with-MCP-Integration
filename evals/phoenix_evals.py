"""
Phoenix LLM-as-a-Judge Evaluators for the Research Agent pipeline.

Evaluates three components:
  1. ReWOO Planner   — did it generate a sensible research plan?
  2. Summarize Agent — is the summary clear and coherent?
  3. Fact-Check Agent — is the confidence score meaningful / not degraded?

Usage:
  # Run a few queries first to populate Phoenix with spans
  make query Q="What is the A2A protocol?"
  make query Q="What is Model Context Protocol?"

  # Then run evals
  make evals

  # Open Phoenix to see scores attached to each span
  make phoenix
"""

import argparse
import os
import sys
import warnings

import phoenix as px
from openinference.instrumentation import suppress_tracing
from phoenix.evals import OpenAIModel, llm_classify
from phoenix.trace import SpanEvaluations
from phoenix.trace.dsl import SpanQuery

warnings.filterwarnings("ignore", category=DeprecationWarning)
warnings.filterwarnings("ignore", category=UserWarning, module="phoenix")

# ── Config ────────────────────────────────────────────────────────────────────

PHOENIX_URL = os.getenv("PHOENIX_BASE_URL", "http://localhost:6006")
PROJECT_NAME = os.getenv("PHOENIX_PROJECT", "default")
OPENAI_KEY = os.getenv("OPENAI_API_KEY", "")
EVAL_MODEL = os.getenv("EVAL_MODEL", "gpt-4o-mini")  # cheap — evals don't need gpt-4o

if not OPENAI_KEY:
    print("ERROR: OPENAI_API_KEY not set — cannot run LLM-as-a-judge evaluators.")
    sys.exit(1)

client = px.Client(endpoint=PHOENIX_URL)
model = OpenAIModel(model=EVAL_MODEL, api_key=OPENAI_KEY)


# ── Prompt templates ──────────────────────────────────────────────────────────

PLAN_QUALITY_PROMPT = """
You are evaluating the quality of a research plan generated by an AI planning agent.

The agent was given a research query and produced a step-by-step plan of tool calls
(search, summarize) to answer it. Your job is to assess whether the plan is sensible
and appropriate for the query.

A CORRECT plan:
- Starts with at least one search step to gather information
- Ends with a summarize step to synthesize findings
- Has steps that are logically ordered and relevant to the query
- Does not include unnecessary or redundant steps

An INCORRECT plan:
- Skips searching and goes straight to summarize with no data
- Has steps unrelated to the query
- Is missing the summarize step entirely
- Has circular or contradictory steps

[BEGIN DATA]
Query: {question}
Plan (JSON): {plan}
[END DATA]

Analyze the plan carefully. Does it make sense for answering the query?

EXPLANATION: Provide step-by-step reasoning about whether each plan step is appropriate.
LABEL: "correct" or "incorrect"
"""

CLARITY_PROMPT = """
You are evaluating the clarity and quality of a research summary produced by an AI agent.

A CLEAR summary:
- Directly addresses the research question
- Is well-structured and easy to read
- Cites or references specific information (e.g. dates, names, facts)
- Is factual and avoids vague generalisations
- Is at least 2-3 sentences long

An UNCLEAR summary:
- Is vague or generic without specifics
- Does not address the research question
- Contains filler phrases like "Based on the search results..."
- Is too short to be useful (e.g. "No results to summarise.")
- Contains contradictory or confusing statements

[BEGIN DATA]
Research query: {question}
Summary: {response}
[END DATA]

Analyze whether the summary clearly and usefully answers the research query.

EXPLANATION: Explain specifically what makes the summary clear or unclear.
LABEL: "clear" or "unclear"
"""

SEARCH_QUALITY_PROMPT = """
You are evaluating the quality of web search results returned by a search agent.

Given a search query and the results metadata (number of results, backend used),
assess whether the search appears to have been successful and useful.

A GOOD search result:
- Returns at least 1-2 results (num_results > 0)
- Uses a reliable backend (tavily or duckduckgo — not scrape_fallback with 0 results)
- The backend is not "none"

A POOR search result:
- Returns 0 results regardless of backend
- Uses scrape_fallback with 0 results (indicates rate limiting or blocking)
- Backend is "none" (total failure)

[BEGIN DATA]
Query: {question}
Number of results: {num_results}
Backend used: {backend}
Cached: {cached}
[END DATA]

EXPLANATION: Explain whether the search returned useful results.
LABEL: "good" or "poor"
"""


# ── Evaluator 1: ReWOO Planner quality ───────────────────────────────────────


def eval_planner(dry_run: bool = False) -> int:
    print("\n── Evaluating ReWOO Planner ──────────────────────────────────────")

    try:
        spans_df = client.query_spans(
            SpanQuery()
            .where("name == 'rewoo_planner.plan' or name == 'ReWOOPlanner.plan'")
            .select(
                question="input.query",
                plan="output.plan",
            ),
            project_name=PROJECT_NAME,
            timeout=None,
        )
    except Exception as exc:
        print(f"  Could not query planner spans: {exc}")
        return 0

    if spans_df is None or spans_df.empty:
        print("  No planner spans found — run some queries first (make query Q='...')")
        return 0

    spans_df = spans_df.dropna(subset=["question", "plan"])
    print(f"  Found {len(spans_df)} planner spans to evaluate")

    if spans_df.empty:
        print("  No planner spans with both question and plan — skipping")
        return 0

    if dry_run:
        print("  [dry-run] Skipping LLM classification")
        return len(spans_df)

    with suppress_tracing():
        eval_df = llm_classify(
            data=spans_df,
            template=PLAN_QUALITY_PROMPT,
            rails=["correct", "incorrect"],
            model=model,
            provide_explanation=True,
        )

    eval_df["score"] = eval_df["label"].map({"correct": 1, "incorrect": 0})

    correct = eval_df["score"].sum()
    total = len(eval_df)
    print(f"  Result: {correct}/{total} plans correct ({100 * correct // total}%)")
    print(f"  Breakdown:\n{eval_df['label'].value_counts().to_string()}")

    client.log_evaluations(SpanEvaluations(eval_name="Plan Quality", dataframe=eval_df))
    print("  ✓ Logged 'Plan Quality' evaluations to Phoenix")
    return total


# ── Evaluator 2: Summary clarity ──────────────────────────────────────────────


def eval_summary_clarity(dry_run: bool = False) -> int:
    print("\n── Evaluating Summary Clarity ────────────────────────────────────")

    try:
        spans_df = client.query_spans(
            SpanQuery()
            .where("name == 'summarize_agent.summarize'")
            .select(
                question="input.query",
                response="output.summary_preview",
                num_results="input.num_results",
                tokens="output.input_tokens",
            ),
            project_name=PROJECT_NAME,
            timeout=None,
        )
    except Exception as exc:
        print(f"  Could not query summarize spans: {exc}")
        return 0

    if spans_df is None or spans_df.empty:
        print("  No summarize spans found")
        return 0

    spans_df = spans_df.dropna(subset=["question", "response"])
    # Skip degraded responses — no point judging "No results to summarise."
    spans_df = spans_df[~spans_df["response"].str.startswith("No results")]
    print(f"  Found {len(spans_df)} summary spans to evaluate")

    if spans_df.empty:
        print("  No summary spans to evaluate — skipping")
        return 0

    if dry_run:
        print("  [dry-run] Skipping LLM classification")
        return len(spans_df)

    with suppress_tracing():
        eval_df = llm_classify(
            data=spans_df,
            template=CLARITY_PROMPT,
            rails=["clear", "unclear"],
            model=model,
            provide_explanation=True,
        )

    eval_df["score"] = eval_df["label"].map({"clear": 1, "unclear": 0})

    clear = eval_df["score"].sum()
    total = len(eval_df)
    print(f"  Result: {clear}/{total} summaries clear ({100 * clear // total if total else 0}%)")
    print(f"  Breakdown:\n{eval_df['label'].value_counts().to_string()}")

    client.log_evaluations(SpanEvaluations(eval_name="Summary Clarity", dataframe=eval_df))
    print("  ✓ Logged 'Summary Clarity' evaluations to Phoenix")
    return total


# ── Evaluator 3: Fact-check confidence (rule-based) ──────────────────────────


def eval_fact_check(dry_run: bool = False) -> int:
    print("\n── Evaluating Fact-Check Confidence ─────────────────────────────")

    try:
        spans_df = client.query_spans(
            SpanQuery()
            .where("name == 'fact_check_agent.verify'")
            .select(
                confidence="output.confidence",
                verified="output.verified",
                num_flags="output.num_flags",
                degraded="output.degraded",
            ),
            project_name=PROJECT_NAME,
            timeout=None,
        )
    except Exception as exc:
        print(f"  Could not query fact-check spans: {exc}")
        return 0

    if spans_df is None or spans_df.empty:
        print("  No fact-check spans found")
        return 0

    print(f"  Found {len(spans_df)} fact-check spans to evaluate")

    # Rule-based scoring — no LLM needed
    def score_fact_check(row) -> tuple[str, int]:
        if row.get("degraded") is True:
            return "degraded", 0
        conf = row.get("confidence")
        if conf is None:
            return "degraded", 0
        if conf >= 0.75:
            return "high_confidence", 1
        if conf >= 0.40:
            return "medium_confidence", 1
        # ChromaDB empty → confidence=0.0, not a failure per se
        if conf == 0.0:
            return "kb_empty", 0
        return "low_confidence", 0

    results = spans_df.apply(score_fact_check, axis=1)
    spans_df["label"] = results.apply(lambda x: x[0])
    spans_df["score"] = results.apply(lambda x: x[1])

    print(f"  Result: {spans_df['score'].sum()}/{len(spans_df)} meaningful confidence scores")
    print(f"  Breakdown:\n{spans_df['label'].value_counts().to_string()}")
    print(
        "  NOTE: kb_empty is expected on first runs — confidence improves as ChromaDB is populated"
    )

    if not dry_run:
        eval_df = spans_df[["label", "score"]].copy()
        client.log_evaluations(
            SpanEvaluations(eval_name="Fact Check Confidence", dataframe=eval_df)
        )
        print("  ✓ Logged 'Fact Check Confidence' evaluations to Phoenix")

    return len(spans_df)


# ── Evaluator 4: Search quality (rule-based) ──────────────────────────────────


def eval_search_quality(dry_run: bool = False) -> int:
    print("\n── Evaluating Search Quality ─────────────────────────────────────")

    try:
        spans_df = client.query_spans(
            SpanQuery()
            .where("name == 'search_agent.search'")
            .select(
                question="input.query",
                num_results="output.num_results",
                backend="output.backend",
                cached="output.cached",
            ),
            project_name=PROJECT_NAME,
            timeout=None,
        )
    except Exception as exc:
        print(f"  Could not query search spans: {exc}")
        return 0

    if spans_df is None or spans_df.empty:
        print("  No search spans found")
        return 0

    spans_df = spans_df.dropna(subset=["num_results", "backend"])
    print(f"  Found {len(spans_df)} search spans to evaluate")

    def score_search(row) -> tuple[str, int]:
        num = row.get("num_results", 0)
        back = row.get("backend", "none")
        if num > 0 and back in ("tavily", "duckduckgo", "cache"):
            return "good", 1
        if num > 0 and back == "scrape_fallback":
            return "degraded", 0
        return "poor", 0

    results = spans_df.apply(score_search, axis=1)
    spans_df["label"] = results.apply(lambda x: x[0])
    spans_df["score"] = results.apply(lambda x: x[1])

    good = spans_df["score"].sum()
    total = len(spans_df)
    print(f"  Result: {good}/{total} searches successful ({100 * good // total if total else 0}%)")
    print(f"  Breakdown:\n{spans_df['label'].value_counts().to_string()}")

    if not dry_run:
        eval_df = spans_df[["label", "score"]].copy()
        client.log_evaluations(SpanEvaluations(eval_name="Search Quality", dataframe=eval_df))
        print("  ✓ Logged 'Search Quality' evaluations to Phoenix")

    return total


# ── Main ──────────────────────────────────────────────────────────────────────


def main():
    parser = argparse.ArgumentParser(description="Run Phoenix evaluators for Research Agent")
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Query spans but skip LLM classification (free — just counts)",
    )
    parser.add_argument(
        "--only",
        choices=["planner", "summary", "factcheck", "search"],
        help="Run only one evaluator",
    )
    args = parser.parse_args()

    print(f"Phoenix URL:   {PHOENIX_URL}")
    print(f"Project:       {PROJECT_NAME}")
    print(f"Eval model:    {EVAL_MODEL}")
    print(f"Dry run:       {args.dry_run}")

    total = 0

    if args.only in (None, "planner"):
        total += eval_planner(args.dry_run)

    if args.only in (None, "summary"):
        total += eval_summary_clarity(args.dry_run)

    if args.only in (None, "factcheck"):
        total += eval_fact_check(args.dry_run)

    if args.only in (None, "search"):
        total += eval_search_quality(args.dry_run)

    print(f"\n{'─' * 60}")
    print(f"Total spans evaluated: {total}")
    print(f"Open Phoenix to see results: {PHOENIX_URL}")
    print(f"{'─' * 60}")


if __name__ == "__main__":
    main()
